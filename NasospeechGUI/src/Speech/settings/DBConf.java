    private void writeConnectionProperties() {
        try {
            Properties props = new Properties();
            props.setProperty("host", txtHost.getText() + ":" + txtPort.getText());
            props.setProperty("database", txtDBName.getText());
            props.setProperty("dbuser", txtUser.getText());
            props.setProperty("dbpassword", txtPws.getText());
            File file = new File("conf/db/dbconfig.properties");
            FileOutputStream fileOut = new FileOutputStream(file);
            props.store(fileOut, "Data Base Settings");
            fileOut.close();




        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error : " + e, "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }

    private void getConnectionInfo() {
        ConnectionProperties connPro = new ConnectionProperties();
        if (connPro.getDbProperties()) {
            String host = connPro.getHostName();
            String[] hostPort = host.split(":");
            if (hostPort.length == 2) {
                txtHost.setText(hostPort[0]);
                txtPort.setText(hostPort[1]);
            }
            txtDBName.setText(connPro.getDbName());
            txtPws.setText(connPro.getPassword());
            txtUser.setText(connPro.getUserName());
        }
    }

